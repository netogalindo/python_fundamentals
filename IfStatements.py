day_of_the_week = input("What day of the week is it today? ").lower()

if day_of_the_week == "monday":
    print("Today is Monday! Have a great week!") # The indentation is what defines this if statement in Python
elif day_of_the_week == "tuesday":
    print("You still have quite some days ahead!")
else:
    print("The week gets closer to its end at every passing day!")

